    private $id;

    /**
     * @var string
     */
    private $firstName;

    /**
     * @var string
     */
    private $lastName;

    /**
     * @var string
     */
    private $nickName;

    /**
     * @var string
     */
    private $email;

    /**
     * @var string
     */
    private $password;

    /**
     * @var \DateTime
     */
    private $birthdate;

    /**
     * @var boolean
     */
    private $gender;

    /**
     * @var string
     */
    private $avatar;

    /**
     * @var string
     */
    private $statut;


    /**
     * Get id
     *
     * @return integer 
